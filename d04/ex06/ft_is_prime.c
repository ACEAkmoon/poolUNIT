int	ft_is_prime(int nb)
{
	int q;

	q = 3;
	if (nb == 2 || nb == 3)
		return (1);
	if (nb <= 1 || nb % 2 == 0 || nb % 3 == 0)
		return (0);
	while ((q * q) <= nb)
	{
		if ((nb % q) == 0)
			return (0);
		q += 2;
	}
	return (1);
}
