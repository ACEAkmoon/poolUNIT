int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	i;
	int	tmp;

	i = 0;
	tmp = 0;
	while (i < length - 1 && tmp <= 0)
	{
		tmp = f(tab[i], tab[i + 1]);
		i++;
	}
	tmp = (tmp <= 0) ? 1 : 0;
	return (tmp);
}
