int		ft_atoi(char *str)
{
	int q;
	int w;
	int j;

	q = 0;
	w = 1;
	j = 0;
	if ((str[w] == '\0') && ((str[q] >= 45) && (str[q] <= 57)))
	{
		j += str[q] - 48;
		return (j);
	}
	while ((str[q] >= 48) && (str[q] <= 57))
	{
		j += str[q] - 48;
		if  ((str[w] >= 48)	&&	(str[w] <= 57))
		{
			j *= 10;
		}
		q++;
		w++;
	}
	return (j);
}
